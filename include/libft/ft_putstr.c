#include "libft.h"

int	ft_putstr(char *str)
{
	int	c;

	c = 0;
	if (str == NULL)
	{
		write (1, "(null)", 6);
		return (c + 6);
	}
	while (str[c] != '\0')
	{
		write(1, &str[c], 1);
		c++;
	}
	return (c);
}
